package sistema;

public class Administrador {
	private int cod;
	private String nome;
	
	public Administrador() {
		
	}
	
	public int getCod() {
		return cod;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setCod(int cod) {
		this.cod = cod;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
}
